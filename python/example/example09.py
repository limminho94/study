# 두 수 중 큰 수 찾기
#
# 사용자로부터 입력 받은 두 수를 변수 a, b에 저장하고, a 값이 b 값보다 크면 a 값을 출력하고, 그렇지 않으면 b 값을 출력한다.

a = input()   # 사용자로부터 입력 받은 수를 변수 a에 저장한다.
b = input()   # 사용자로부터 입력 받은 수를 변수 b에 저장한다.
if (a > b) :      # a 값이 b 값보다 크면
    print(a)      # a 값을 출력한다.
else :            # 그렇지않으면
    print(b)      # b 값을 출력한다.